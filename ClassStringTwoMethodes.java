public class ClassStringTwoMethodes {
    public static void main(String[] args) {
        // Use Method : charAt(index)
        // Use Method : indexOf(char)
        String S;
        S = "Hello";
        System.out.println(S);
        System.out.println("charAt(index: 0)"+S.charAt(0)); //output : H
        System.out.println("indexOf(char: e)"+S.indexOf("e")); //output : 1
    }
}